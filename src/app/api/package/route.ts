import { NextRequest, NextResponse } from 'next/server';

const API_BASE_URL = 'https://edueye.co.in/ensurekar/existing-site/package.php';

export async function GET(request: NextRequest) {
  try {
    const { searchParams } = new URL(request.url);
    const id = searchParams.get('id');
    
    // Build URL - if ID is provided, add it as query parameter
    let apiUrl = API_BASE_URL;
    if (id) {
      apiUrl = `${API_BASE_URL}?id=${id}`;
    } 
    
    const response = await fetch(apiUrl, {
      method: 'GET',
      headers: {
        'Content-Type': 'application/json',
      },
      cache: 'no-store',
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error('ðŸ”µ [API Route] API error response:', errorText);
      return NextResponse.json(
        { error: 'Failed to fetch packages', details: errorText },
        { status: response.status }
      );
    }

    const data = await response.json();

    // Return with CORS headers
    return NextResponse.json(data, {
      status: 200,
      headers: {
        'Access-Control-Allow-Origin': '*',
        'Access-Control-Allow-Methods': 'GET, POST, PUT, DELETE, OPTIONS',
        'Access-Control-Allow-Headers': 'Content-Type',
      },
    });
  } catch (error) {
    console.error('ðŸ”µ [API Route] Error fetching packages:', error);
    return NextResponse.json(
      { error: 'Internal server error', details: error instanceof Error ? error.message : 'Unknown error' },
      { status: 500 }
    );
  }
}

